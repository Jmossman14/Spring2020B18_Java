package Day02_Variables;

public class StringPractice {
    public static void main(String[] args) {
        String SSN = "123456789"; // Text, not a #//
        String employeeName = "Judy Mossman";
        String CompanyName = "ABC Co";
        System.out.println(employeeName);
        System.out.println("SSN + employeeName");
        System.out.println(employeeName+" Works at "+CompanyName);

        // + adds different strings, " " will allow to add additional text //
    }
}
