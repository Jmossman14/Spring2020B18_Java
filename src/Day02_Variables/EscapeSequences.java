package Day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("\tHello");
        System.out.println("\t\t\t\t\t\tHELLO");
        // \t = paragraph space

        System.out.println("Cybertek \nSchool");
        System.out.println("My\nName\n\nis\nJudy\nMossman");
        // \n = New line without having to add another SOUT

        System.out.println("My favorite TV show is \"Law and Order: SVU\"");
        System.out.println ("My favorite book is \"Power vs Force\"");
        // \" = Adding "" to a print

        System.out.println("\\");
        // \\ = in order to print single FRONT slash you need 2 (not needed for back slash)

        System.out.println("My favorite name is 'Keora'");
        System.out.println("My favorite name is \'Keora\'");
        // \\ ' or \' = adds a single quote


    }



}
