package Day17_While_DoWhile_Loop;

public class WhileLoop_Practice3 {
    public static void main(String[] args) {
        /*
               ***
               * *
               * *
               * *
               ***
         */
        int num = 0;
        System.out.println("***");
        while(num > 3){
            System.out.println("* *");
            num--;
        }
        System.out.println("***");
    }
}
